    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Erro! Selecione um Aluno");
        } else {
            //JOptionPane.showConfirmDialog(null,"Deja mesmo apagar este aluno??");
            cdAluno.eliminar(Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString()));
            lerTabela();
            limpar();
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    public void lerTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        for (Aluno a : cdAluno.lista1()) {
            modelo.addRow(new Object[]{a.getNumAluno(), a.getNome(), a.getCategoria()});
        }
    }

    public void lerTabelaNovosIngressos() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        for (Aluno a : cdAluno.lista1()) {
            if (a.getCategoria().equals("Novo ingresso")) {
                modelo.addRow(new Object[]{a.getNumAluno(), a.getNome(), a.getCategoria()});
            }

        }

    }

    public void lerTabelaRepitentes() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        for (Aluno a : cdAluno.lista1()) {
            if (a.getCategoria().equals("Repitente")) {
                modelo.addRow(new Object[]{a.getNumAluno(), a.getNome(), a.getCategoria()});
            }

        }

    }

    public void lerTabelaTransferido() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        for (Aluno a : cdAluno.lista1()) {
            if (a.getCategoria().equals("Transferido")) {
                modelo.addRow(new Object[]{a.getNumAluno(), a.getNome(), a.getCategoria()});
            }

        }

    }

    public void limpar() {
        txtPesquisa.setText("");
        txtNome.setText("");
        lerTabela();
    }

