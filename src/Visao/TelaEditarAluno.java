    private void tabelaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseReleased
        // TODO add your handling code here:
        txtNome.setText(tabela.getValueAt(tabela.getSelectedRow(), 1).toString());
        if (tabela.getValueAt(tabela.getSelectedRow(), 2).toString().equals("Novo ingresso")) {
            cmboCategoria2.setSelectedIndex(0);
        } else if (tabela.getValueAt(tabela.getSelectedRow(), 2).toString().equals("Repitente")) {
            cmboCategoria2.setSelectedIndex(1);
        } else if (tabela.getValueAt(tabela.getSelectedRow(), 2).toString().equals("Transferido")) {
            cmboCategoria2.setSelectedIndex(2);
        }
    }//GEN-LAST:event_tabelaMouseReleased

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Erro! Selecione um Aluno");
        } else {
            a.setNome(txtNome.getText().toUpperCase());
            a.setNumAluno(Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString()));
            a.setCategoria((String) cmboCategoria2.getSelectedItem());
            cdAluno.editar(Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString()), a);
            lerTabela();
            limpar();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

